预计：大概10月？号考试。
！！！疑惑：直角转弯灯，发动机左缝对其左侧黄先？，车头快到水泥边时候快速右打满？车头到三个水泥边时候转弯？！！！
  二调，一系，三检查=》调座椅，调三个后视镜，系安全带，检查发动机转速表，检查空档否，检查手刹（空调打开）。
  钥匙口：关，供电，发动机启动状态，点火发动机。
  考场步骤=》倒车入库，坡道停车起步，侧方停车，曲线，直角转弯。
  停车：首先要按住离合器，慢慢轻踩刹车。
  开车要慢速和均速。方向盘的转圈要记住，停车后要摆正。（方向盘要一拉一推的方法。）转弯状态不能用转向灯。

错误解决：
  出错了多练习几次就可以了，这种想法是错误的。练习两次100%做好，哪里觉得不对就下车看问题出在哪里？自己想怎么结局，解决不了的问教练。教练可以知道错在哪里，解决根本的错误。让你更快的解决问题。

*倒车入库：
  ~~~中途停车扣5分。车身出线，不按规定路线顺序行驶，没有完全倒入车库扣100分。~~~
  车开到车库的右方离外边线1.5-2m之间，汽车前轮要过虚线，汽车与车库门平行。缓慢倒车左后视镜刚过黄虚线时候方向盘快速右打满，继续缓慢倒车，左后视镜刚压住虚线时停车。挂一档，当车头挡住黄线时候，向左打满，车头盖过黄线时停车（不许回正方向盘）。挂倒挡低速入库。

*上皮与起步：
  ！！！第一次抖动时候稳定住离合器脚不要在动了，听见第二抖动时候慢慢放制动。对准黄色排气管线，对齐白色箭头的右侧。起步时候两次抖动一下时候迅速慢慢抬制动器。

*侧方停车：
  ~~~中途停车扣5分。出库时候用转向灯，否则扣10分。~~~
  挂一档，调整车身与右侧距离30cm左右，前轮虚线后停车。挂倒挡缓慢向后行驶，右后视镜后轮与直角对其时（右后视镜刚过直角时候）右打满，左后视镜车库后端线全部露出后回正，车后轮压住虚线时左打满，车身摆正时停车，不许回正方向。挂一档打左转向灯出车库，看左后视镜车库后端线全部露出时，回正右打一圈，车身平齐车道驶出。

*曲线：
  车对准曲线入口，两边冷角始终在贴着曲线以内行驶，左冷角快到左边线时候迅速回正。曲线与车身中间时候右打一圈。
  
*直角转弯：
  左侧发动机盖缝贴着黄线中间行驶，打右转灯，发动机前盖正好压住黄线时候迅速右转。

