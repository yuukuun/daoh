<h1><a href="../index.html">科目二</a></h1><div class="row"><span class="texts">
<div onclick="play('../m/教练录音.mp3')" class="tit col-12 col-md-12" style="text-align: center;"><a href="#q">教练_考试要点录音_10.28</a></div>
11月02号考试：
！！！考试车是三两，考试时候车辆是着火状态。！！！
！！！车身在肩膀对齐水泥点，车身与库平行的位置，这个位置输入指纹考试正式开始！！！
！！！幻想科目二过以后就快乐的练习科目三了。！！！
！！！满分是100分，发动机熄火一次扣10分，项目中途停车超过两秒的一次扣5分，不超过两秒就不扣分。！！！
！！！用离合器一定要控制车速要慢慢的，就算停车也可以。用脚中下去控制车速。| 车速不能快，只能慢速，慢了停车也没事，快了就慌乱更紧张。| 每个项目完成后可以停车，倒车，可以压线，但是不能熄火，别越过前一个项目！！！

准本：
  首先上车后调好座椅，三个后视镜，系安全带，（座椅，镜子，安全带关系到全局）检查发动机表（考场默认状态是启动），是否在空档。当车开到倒车入库项目旁边，刹车和离合器踩好后输入指纹完成后，正是开始考试。

第一 倒车入库：
  对准第五个黄色虚线缓慢行驶，车身一定要摆正，虚线跟肩膀对其后停车。（听到考试指令后开始计时）挂倒挡缓慢倒车左边镜子正好盖住虚线时候右打满，看右边镜子观察库角（根据库角与车身间距来调整方向盘，以30cm为参考），车身摆正时候方向盘回正，镜子在盖住虚线露出一点时候停车。换一档缓慢使出，当车头离黄线10cm时候左打满，当车启动开始后方向盘正朝下，前轮过虚线左手和方向盘对齐虚线停车，换倒挡缓慢倒车，看左边镜子观察库角（根据库角与车身间距来调整方向盘，以30cm为参考），车身摆正时候方向盘回正，镜子在盖住虚线露出一点时候停车。换一档使出本项目，车身驶过虚线完成项目。（车身出线不合格）

第二 坡道定点停车与起步：
  车上坡前还没过考场指示字（坡道...）项目还没开始之前可以调整车方向盘对准箭头右侧，引擎盖左棱角对准箭头左侧。上去时候可以微调。（听到考试指令坡道项目开始）离合器稍微抬一下上坡，车头与黄色实线对齐停车，挂手刹。缓慢胎离合器，当车身有往前冲的感觉时候不要顾忌加油门，放手刹起步（不要着急起步，监听车身）。下坡时候不要空档滑车和注意熄火，保持半离合状态就行。

第三 侧方停车：
  往前开车，项目还没开始之前可以倒车，压线，调整车身。当稍微看见点箭头（个子矮看不见箭头）时候停车，无能越过白箭头。（听到考试指令后侧方停车开始计时）缓慢倒车右后视镜看不见库角时右打满，迅速看左后视镜当看见右库角时（水泥角也可以），方向盘快速回正。缓慢倒车观察左后视镜观察左后轮正好要压线时，方向盘左打满，车身摆正后停车，挂挡。先打左转向灯，在启动车身缓慢使出，当左棱快到边黄色实线时候回正方向盘，黄线快到车身中间时候右打一圈方向，往前开到一定要听到项目完成声音后才算结束本项目，还没听到本项目结束时候停车都算直接不合格。（车身出线不合格）

第四 曲线： 
  进入曲线之前区域是自由区域，车对准后停车等候。（听到考试指令坡道项目开始）开车前进左棱角快到右边实现时候方向盘左打一圈。缓慢行驶至，左棱角快到左边实线时候回正方向盘。当看见黄色实线在车身中时候右打满一圈，使出曲线听到完成曲线后停车等候下个项目。（注意：进入曲线前一定要是确保曲线和直角没有车辆。）

第五 直角： 
  调整车身左侧引擎盖缝正好压在黄色实现上。（听到考试指令坡道项目开始）缓慢行驶，打右转向灯，用左手往上抬起转向灯，车头刚好压过黄实线时候，右边打满，车身快要摆正前左回半圈转向灯自动灭，往前行驶至项目结束。

结束：
  直角项目完成后路侧停好车，拉手刹。有提示及格也先别下车，等候考试信息上传完成，考生资料消失后方可下车。（圆满结束）

